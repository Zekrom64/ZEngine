package com.zekrom_64.ze.base.input;

/** Interface providing a boolean input value from the user
 * 
 * @author Zekrom_64
 *
 */
public interface ZEBoolInput {

	/** Gets the input state as a boolean.
	 * 
	 * @return Boolean state value
	 */
	public boolean getBoolInputState();
	
	/** Sets the input state as a boolean.
	 * 
	 * @param state Boolean state value
	 */
	public void setBoolInputState(boolean state);
	
}
