package com.zekrom_64.ze.voxel;

/** Enumeration for the 3 different axis
 * 
 * @author Zekrom_64
 *
 */
public enum ZEAxis {

	/** The X axis */
	X,
	/** The Y axis */
	Y,
	/** The Z axis */
	Z;
	
	/** Faster way of accessing all the values */
	public static final ZEAxis[] values = new ZEAxis[] {X, Y, Z};
	
}
